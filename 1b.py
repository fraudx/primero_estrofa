n1 = int(input("ingrece el numero 1 "))
n2 = int(input("ingrece el numero 2 "))

suma = n1 + n2
resta = n1 - n2
producto = n1 * n2
divicion = n1 / n2


print(str(n1) + " + "+ str(n2) + "=" + str(suma) )
print(str(n1) + " - "+ str(n2) + "=" + str(resta) )
print(str(n1) + " * "+ str(n2) + "=" + str(producto) )
print(str(n1) + " / "+ str(n2) + "=" + str(divicion) )
